export function getRoleModelById(id: string): RoleModel | undefined {
  return ROLE_MODELS.find(model => model.id === id);
}

export function searchRoleModels(query: string): RoleModel[] {
  if (!query) return ROLE_MODELS;

  const lowerQuery = query.toLowerCase();
  return ROLE_MODELS.filter(model =>
    model.name.toLowerCase().includes(lowerQuery) ||
    model.furigana.toLowerCase().includes(lowerQuery) ||
    model.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    model.catchphrase.toLowerCase().includes(lowerQuery)
  );
}

export function filterRoleModelsByTag(tag: string): RoleModel[] {
  if (!tag || tag === 'all') return ROLE_MODELS;
  return ROLE_MODELS.filter(model => model.tags.includes(tag));
}

export function getAllTags(): string[] {
  const tagSet = new Set<string>();
  ROLE_MODELS.forEach(model => {
    model.tags.forEach(tag => tagSet.add(tag));
  });
  return Array.from(tagSet).sort();
}
